package com.consumer;

public class Movie {
	String name;
	int duration;
	String hero;
	String heroin;
	String result;
	
	public Movie(String name, int duration, String hero, String heroin, String result) {
		super();
		this.name = name;
		this.duration = duration;
		this.hero = hero;
		this.heroin = heroin;
		this.result = result;
	}
}
