package com.biInterface;

public class Employee {
	int eno;
	String name;
	double dailyWage;
	
	/**
	 * 
	 * @param eno
	 * @param name
	 * @param dailyWage
	 */
	public Employee(int eno, String name, double dailyWage) {
		super();
		this.eno = eno;
		this.name = name;
		this.dailyWage = dailyWage;
	}
}
